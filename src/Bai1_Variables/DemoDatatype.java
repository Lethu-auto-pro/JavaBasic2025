package Bai1_Variables;

public class DemoDatatype {

    public static void main(String[] args) {

        String customer_name ="Iu Thọ gất nhìu";
        System.out.println(customer_name);

        char kitu = 't';
        System.out.println(kitu);

        long songuyen = 214748364880007665L;
        System.out.println(songuyen);

        float sothapphan = 10.5F;
        double sothapphan1 = 10.185757666D;
        System.out.println(sothapphan);
        System.out.println(sothapphan1);


    }
}
